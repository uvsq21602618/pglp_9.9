package fr.uvsq.uvsq21602618.pglp_9_9;
/**
 * Classe pour un point
 * @author Nathalie
 */
public class Point {

    private int x;
    private int y;
    
    public Point(final int x2, final int y2) {
        this.x = x2;
        this.y= y2;
    }
    
    public int getX() {
        return this.getX();
    }
    
    public int getY() {
        return this.getY();
    }
}
